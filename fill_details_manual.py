def main():
    json_files = [
        f for f in glob.glob(os.path.join(ANALYSES_DIR, "*.json")) if not f.endswith(".bak")
    ]
    json_files.sort()

    total_updated = 0
    files_modified = 0

    for filepath in json_files:
        with open(filepath, encoding="utf-8") as f:
            data = json.load(f)

        modified = False
        for analysis in data.get("analyses", []):
            for word_obj in analysis.get("words", []):
                w = word_obj.get("word", "")
                detail = word_obj.get("meaning_detail", "")
                if not detail.strip() and w in DETAIL_MAP:
                    word_obj["meaning_detail"] = DETAIL_MAP[w]
                    modified = True
                    total_updated += 1

        if modified:
            # .bak 백업
            bak_path = filepath + ".bak"
            if not os.path.exists(bak_path):
                shutil.copy2(filepath, bak_path)
            with open(filepath, "w", encoding="utf-8") as f:
                json.dump(data, f, ensure_ascii=False, indent=2)
            files_modified += 1
            basename = os.path.basename(filepath)
            print(f"  [OK] {basename}")

    print(f"\n완료: {files_modified}개 파일 수정, {total_updated}건 meaning_detail 채움")

    # 남은 빈 항목 확인
    remaining = set()
    for filepath in json_files:
        with open(filepath, encoding="utf-8") as f:
            data = json.load(f)
        for analysis in data.get("analyses", []):
            for word_obj in analysis.get("words", []):
                if not word_obj.get("meaning_detail", "").strip():
                    remaining.add(word_obj["word"])
    if remaining:
        print(f"\n아직 빈 meaning_detail: {len(remaining)}개")
        for w in sorted(remaining):
            print(f"  - {w}")
    else:
        print("\n빈 meaning_detail 없음 (모두 채움)")


if __name__ == "__main__":
    main()
